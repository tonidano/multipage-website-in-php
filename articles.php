      <div class="card-header"><strong>« Namur… et nous! » Projection des 8 capsules</strong></div>
      <div class="card-body">
        <p style="text-align:justify;">A l’initiative de l’échevinat de la Jeunesse de la Ville de Namur, Loupiote a animé et encadré, durant l’année 2017, plusieurs ateliers de création de capsules vidéo. Intitulé «Namur… et nous!», le projet a ainsi fédéré 8 associations de jeunesse autour d’un même objectif: donner l’opportunité aux jeunes de pouvoir s’exprimer sur des thématiques citoyennes qui leur sont chères à travers l’improvisation et le jeu face caméra!</p>
        <p><img src="https://loupioteasbl.files.wordpress.com/2017/11/19866755_10213188631020354_1743329408_o.jpg?w=560&amp;h=316" alt="namuretnous"></p>
        <p style="text-align:justify;">Au total, 8 capsules vidéo ont été réalisées grâce à l’implication d’une cinquantaine de jeunes. Autant de questions et de messages lancés au public qui aura le privilège de les découvrir sur grand écran le <strong>22 novembre</strong> prochain à <strong>20h aux abattoirs de Bomel</strong>, à l’occasion de la grande soirée de clôture du projet qui réunira tous les participants.</p>
        <p style="text-align:justify;">Familles, amis, voisins, citoyens namurois et autres… Vous êtes tous les bienvenus pour assister à cette projection hors du commun qui fera aussi la place au débat et à l’échange en présence d’un invité de choix: le réalisateur belge <strong>François Pirot</strong> (<em>Mobile Home</em>, <em>Eurovillage</em>…) qui trouve ses origines dans notre belle province du Luxembourg et qui sait faire se répondre avec passion cinéma, jeunesse et citoyenneté!</p>
        <p style="text-align:justify;">Pour les plus impatients, <em>Pas si seul</em>, la capsule scénarisée et interprétée par les jeunes de l’AMO «Passages» vous est déjà présentée «en avant-première» ci-dessous…</p>
        <p>L’ensemble des vidéos seront bientôt diffusées sur une chaîne VIMEO spécifique accessible via ce lien :&nbsp;<a href="https://vimeo.com/channels/namuretnous" target="_blank" rel="noopener">https://vimeo.com/channels/namuretnous</a></p>
