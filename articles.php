      <div class="card-header"><strong>Stages « Réflexion cinéma et plaisirs de l’eau » à Verviers</strong></div>
      <div class="card-body">
        <p style="text-align:justify;">L’asbl Loupiote organise en partenariat avec «&nbsp;Au fil du corps&nbsp;» deux stages «&nbsp;sport et culture – activités aquatiques et éducation au par le cinéma&nbsp;» à <strong>Heusy (av. du Chêne, 206) durant ce mois d’août 2018</strong>.</p>
        <p style="text-align:justify;">En matinée, les enfants s’adonneront aux plaisirs de l’eau dans une <strong>piscine chauffée à 32°</strong>, tandis que les après-midis seront dédiées à l’exploration en plusieurs étapes des longs-métrages d’animation <em>Le Monde de Dory</em> (<strong>du 6 au 10 août</strong> pour les enfants de <strong>3 à 5 ans</strong>) et <em>Vaiana</em> (<strong>du 13 au 17 août</strong> pour les enfants de <strong>5 à 8 ans</strong>). La garderie sera ouverte dès 8h30 et fermera ses portes à 16h30.</p>
        <p>
          <a href="https://www.aufilducorps.be/harmonie-aquatique/stages-enfants-ao%C3%BBt-2018/"><img src="https://loupioteasbl.files.wordpress.com/2018/05/le-monde-de-dory-affiche.jpg?w=271&amp;h=369" alt="lemondedeDory"></a>
          <a href="https://www.aufilducorps.be/harmonie-aquatique/stages-enfants-ao%C3%BBt-2018/"><img  src="https://loupioteasbl.files.wordpress.com/2018/05/f-vaiana.jpg?w=258&amp;h=369" alt="vaiana"></a></p>
        <p style="text-align:justify;">Loupiote proposera donc chaque jour un <strong>atelier d’expression créative</strong> en lien avec le contenu du film et adapté à l’âge des participants.</p>
        <p style="text-align:justify;">Via le débat et la réflexion collective, les enfants seront amenés à développer une attitude active et critique face au cinéma qui, bien plus qu’un divertissement, les aidera aussi à se familiariser à l’eau ou à perfectionner des nages en s’amusant… Tout un programme&nbsp;!</p>
        <p style="text-align:justify;">Infos et inscriptions&nbsp;: cliquez <a href="https://www.aufilducorps.be/harmonie-aquatique/stages-enfants-ao%C3%BBt-2018/"><strong>ici</strong></a>.</p>
