          <div class="card-body">
            <p style="text-align:center;">Une formule originale à destination des petits et des grands dans un cadre récréatif et éducatif!</p>
            <p style="text-align:center;"><img src="https://loupioteasbl.files.wordpress.com/2013/08/15cm_mg_6775.jpg?w=560&amp;h=373" alt="15cm_MG_6775"></p>
            <p style="text-align:justify;">Relancé fin 2007 sous l’impulsion de l’ASBL Loupiote en partenariat avec l’ASBL Clap Education, «&nbsp;Les Samedis du Ciné&nbsp;» est un célèbre ciné-club social, familial et éducatif bruxellois qui propose, <strong>20 samedis par an</strong> (de la mi-septembre à la mi-juin), <strong>dès 10h30 du matin dans 2 salles du Cinéma Vendôme</strong>, 2 séances de cinéma séparant les enfants des adultes à un prix démocratique (<strong>2,5 € /personne</strong>).</p>
            <p style="text-align:justify;">– Pour les adultes: des productions récentes de qualité, des films en avant-première ou inédits, des « Débat-Réa » en présence des réalisateurs ou acteurs.<br>
            – <strong>Pour les enfants à partir de 5 ans </strong>(donc sans adultes ni parents, à l’exception d’accompagnateurs de groupes) : encadrement et animation par l’équipe Loupiote autour de films qui leur sont destinés et que le marché commercial ne leur apporte que rarement. Une <strong>fiche pédagogique</strong> relative au film projeté et rédigée expressément pour les enfants est remise à la sortie.</p>
            <p style="text-align:justify;">Chaque fin de saison, les enfants les plus assidus composent un jury et participent au principe de <strong>remise du «&nbsp;Prix Samedis du Ciné&nbsp;»</strong> par la désignation du «&nbsp;Meilleur film pour la Jeunesse&nbsp;». De quoi responsabiliser et impliquer davantage les jeunes en les invitant à débattre et à argumenter autour de leurs choix cinématographiques.</p>
            <p style="text-align:justify;">Objectifs :</p>
            <p style="text-align:justify;">– Via un prix démocratique, rendre la connaissance du 7ème Art plus accessible aux familles ainsi qu’aux personnes les moins favorisées en leur proposant, dans une ambiance conviviale avec encadrement et animations, une programmation récente de qualité échappant aux blockbusters habituels (ouvrir les esprits, leur proposer une autre façon de voir un cinéma différent, issu du monde entier, leur donner aussi l’occasion de découvrir des productions nationales qui restent encore souvent méconnues du grand public).</p>
            <p style="text-align:justify;">– Dans un cadre récréatif et divertissant, donner aux enfants, à partir de 5 ans, l’occasion de s’exprimer librement sur le film visionné et sur les thématiques qu’il aborde tout en les faisant bénéficier d’une éducation au 7ème Art qui a pour objectif de les encourager à devenir des spectateurs actifs et critiques (nécessité d’une éducation à l’image dans une société où celle-ci prend une place de plus en plus envahissante, notamment auprès des jeunes…).</p>
            <p style="text-align:justify;"><strong>Découvrez le programme complet sur notre site <a href="http://www.samedisducine.be">www.samedisducine.be</a> !<br>
            </strong></p>
